/*! \mainpage Projeto de Laboratórios de Informática II 2013/2014
 * O projeto de LI2 consiste em criar uma aplicação na linguagem de programação \e c,
 * que resolva um puzzle do tipo \e Sopa \e de \e Letras. O projeto foi dividido em duas
 * etapas, cada uma com uma série de tarefas a realizar. Cada tarefa é um pequeno
 * passo para a construção do puzzle.
 *
 * \section sec_1 Etapa 1
 * Esta etapa consiste na realização de de duas tarefas. A
 * Tarefa 1 (implementação de funções de manipulação de  \e strings)
 * e a Tarefa 2 (implementação de um programa de leitura de uma
 *  \e string numa matriz).
 *
 * \subsection subsec_1 Tarefa 1 - Funções de manipulação de strings
 * As funções de manipulações de \e strings implementadas
 * são um versão alternativa das funções já existentes
 * na biblioteca padrão do C. O âmbito das funções é
 * entender o processo da sua análise e conceção, i. e.
 * perceber o que estas funções fazem, para as utilizar
 * com maior eficiência, em particular. De um modo geral servem
 * como preparação para as restantes tarefas do projeto.
 *
 * \subsection subsec_2 Tarefa 2
 *
 * O programa desta tarefa consiste em descobrir
 * uma \e string, dado um conjunto de coordenadas, numa matriz
 * que representa uma sopa de letras. O âmbito deste programa
 * é o entendimento das operações com matrizes, e de igual modo,
 * as funções de manipulação de \e strings servem como preparação
 * para as restantes tarefas do projeto.
 *
 *
 *\section sec_2 Etapa 2
 * Esta etapa consiste na realização de quatro tarefas de forma a complementar a nossa formação. 
 * A tarefa 3 permitir-nos-á ser capazes de verificar se uma dada string está contida num
 * dicionário, a a tarefa 4 permitir-nos-á ser capazes de escrever um programa que 
 * leia uma grelha de letras de um ficheiro e que permita a um utilizador jogar à sopa de letras, 
 * a tarefa 5 permitir-nos-á ser capazes de encontrar automáticamente todas as palavras 
 * que existem numa sopa de letras, e por último, a tarefa 6 permitir-nos-á ser capazes de 
 * gerar uma sopa de letras, contendo um dado conjunto de palavras. 
 * Todas estas tarefas poêm a prova os nosso conhecimentos adquiridos na resolução de 
 * problemas mais complexos, que os problemas da etapa
 * anterior.
 *
 * \subsection subsec_3 Tarefa 3
 * Esta tarefa consiste na implementação de uma função \c existe para procurar uma
 * determinada palavra (\e string) num dicionário.
 *
 * \subsection subsec_4 Tarefa 4
 * O programa desta tarefa permite a um utilizador jogar à sopa de letras,
 * após leitura desta de um ficheiro. A sopa de letras possui dois tipo de
 * movimento base (serpentear e passo de cavalo) que são escolhidos pelo
 * utilizador no início do programa.
 *
 *
 * \subsection subsec_5 Tarefa 5
 * O programa desta tarefa consiste na implementação de um programa que
 * encontra automáticamente todas as palavras de uma dada sopa de letras,
 * segundo um movimento pré-definido pelo utilizador (serpentear e passo
 * de cavalo).
 *
 */

